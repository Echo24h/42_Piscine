int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	while (i <= nb / 2 && (long)i * i <= nb)
	{
		if (((long)i * i) % nb == 0)
			return (i);
		i++;
	}
	return (0);
}
